class mashinhesab():
    def taqsimm(self,a,b):
         print("taqsimm=",a/b)
    def zarbadad(self,a,b):
         print("zarbadad=",a*b)
    def tafriq(self,a,b):
         print("tafriq=",a-b)
    def jam(self,a,b):
        print("jam=",a+b)       
    

